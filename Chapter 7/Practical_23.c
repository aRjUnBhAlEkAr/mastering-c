// sizeof() Operator.
// Example 1 : Operation with a Data types.
#include<stdio.h>

int main(){
    // printing size of all Primitive Data type using sizeof() operator.
    printf("Size of int variable is %d\n", sizeof(int));
    printf("Size of float variable is %d\n", sizeof(float));
    printf("Size of char variable is %d\n", sizeof(char));
    printf("Size of double variable is %d\n", sizeof(double));
    printf("Size of long variable is %d\n", sizeof(long));

    return 0;
}