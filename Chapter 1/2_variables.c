// Variables in C Language .
#include<stdio.h>

int main(){

    // Declaring Varibles.
    int a = 10;     // Integers Variables.
    float b = 5.3;  // Real or Float Variable.
    char c = 'A';  // Character Variables.

    // Printing Stored Value in the Variables.
    printf("Value of A is : %d\n",a); // Printing value of 'a' and %d is use for printing Integer Value.
    printf("Value of B is : %f\n",b); // Printing value of 'b' and %f is use for printing Real or Float Value.
    printf("Value of C is : %c",c);   // Printing value of 'c' and %c is use for printing Character Value.
    return 0;
}